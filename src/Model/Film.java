package Model;

public class Film
{
	String title;
	String description;
	int release_year;
	String language;
	String category;
	int rental_duration;
	float rental_rate;
	int length;
	float replacement_cost;
	String rating;
	String special_features;
	/**
	 * @return the title
	 */
	public String getTitle()
	{
		return title;
	}
	/**
	 * @param title the title to set
	 */
	public void setTitle(String title)
	{
		this.title = title;
	}
	/**
	 * @return the description
	 */
	public String getDescription()
	{
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description)
	{
		this.description = description;
	}
	/**
	 * @return the release_year
	 */
	public int getRelease_year()
	{
		return release_year;
	}
	/**
	 * @param release_year the release_year to set
	 */
	public void setRelease_year(int release_year)
	{
		this.release_year = release_year;
	}
	/**
	 * @return the language
	 */
	public String getLanguage()
	{
		return language;
	}
	/**
	 * @param language the language to set
	 */
	public void setLanguage(String language)
	{
		this.language = language;
	}
	/**
	 * @return the category
	 */
	public String getCategory()
	{
		return category;
	}
	/**
	 * @param category the category to set
	 */
	public void setCategory(String category)
	{
		this.category = category;
	}
	/**
	 * @return the rental_duration
	 */
	public int getRental_duration()
	{
		return rental_duration;
	}
	/**
	 * @param rental_duration the rental_duration to set
	 */
	public void setRental_duration(int rental_duration)
	{
		this.rental_duration = rental_duration;
	}
	/**
	 * @return the rental_rate
	 */
	public float getRental_rate()
	{
		return rental_rate;
	}
	/**
	 * @param rental_rate the rental_rate to set
	 */
	public void setRental_rate(float rental_rate)
	{
		this.rental_rate = rental_rate;
	}
	/**
	 * @return the length
	 */
	public int getLength()
	{
		return length;
	}
	/**
	 * @param length the length to set
	 */
	public void setLength(int length)
	{
		this.length = length;
	}
	/**
	 * @return the replacement_cost
	 */
	public float getReplacement_cost()
	{
		return replacement_cost;
	}
	/**
	 * @param replacement_cost the replacement_cost to set
	 */
	public void setReplacement_cost(float replacement_cost)
	{
		this.replacement_cost = replacement_cost;
	}
	/**
	 * @return the rating
	 */
	public String getRating()
	{
		return rating;
	}
	/**
	 * @param rating the rating to set
	 */
	public void setRating(String rating)
	{
		this.rating = rating;
	}
	/**
	 * @return the special_features
	 */
	public String getSpecial_features()
	{
		return special_features;
	}
	/**
	 * @param special_features the special_features to set
	 */
	public void setSpecial_features(String special_features)
	{
		this.special_features = special_features;
	}
		
}
